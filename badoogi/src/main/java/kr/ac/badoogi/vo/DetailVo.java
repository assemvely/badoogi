package kr.ac.badoogi.vo;

public class DetailVo {

	String storename;
	String address;
	String contact;
	String domain;
	String weekend;
	String weekday;
	String parking;
	String cp_entnum;
	String privateor;
	String licensenumber;
	
	
	
	public String getLicensenumber() {
		return licensenumber;
	}
	public void setLicensenumber(String licensenumber) {
		this.licensenumber = licensenumber;
	}
	public String getPrivateor() {
		return privateor;
	}
	public void setPrivateor(String privateor) {
		this.privateor = privateor;
	}
	public String getCp_entnum() {
		return cp_entnum;
	}
	public void setCp_entnum(String cp_entnum) {
		this.cp_entnum = cp_entnum;
	}
	public String getParking() {
		return parking;
	}
	public void setParking(String parking) {
		this.parking = parking;
	}
	public String getStorename() {
		return storename;
	}
	public void setStorename(String storename) {
		this.storename = storename;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getWeekend() {
		return weekend;
	}
	public void setWeekend(String weekend) {
		this.weekend = weekend;
	}
	public String getWeekday() {
		return weekday;
	}
	public void setWeekday(String weekday) {
		this.weekday = weekday;
	}
	
	
}
